package JavaFoundation;

public class operator_increment_decrement {

	public static void main(String args[]) {
	    int a = 1;
	    int b = 2;
	    int c;
	    int d;

	    c = ++b;
	    d = a++;
	    c++;
	    System.out.println("a = " + a);
	    System.out.println("b = " + b);
	    System.out.println("c = " + c);
	    System.out.println("d = " + d);
	  }
	
}
